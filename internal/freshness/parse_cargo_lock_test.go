package freshness

import (
	"testing"
)

const sampleCargoLock = `# This file is automatically @generated by Cargo.
# It is not intended for manual editing.
version = 4

[[package]]
name = "addr2line"
version = "0.24.2"
source = "registry+https://github.com/rust-lang/crates.io-index"
checksum = "deadbeef"
dependencies = [
 "gimli",
]

[[package]]
name = "tokio"
version = "1.50.0"
source = "registry+https://github.com/rust-lang/crates.io-index"
checksum = "cafebabe"
dependencies = [
 "bytes",
 "mio",
]

[[package]]
name = "serde"
version = "1.0.219"
source = "registry+https://github.com/rust-lang/crates.io-index"
checksum = "abc123"
`

func TestParseCargoLock_BasicVersions(t *testing.T) {
	t.Parallel()
	resolved := ParseCargoLock([]byte(sampleCargoLock))

	tests := []struct {
		name    string
		want    string
		present bool
	}{
		{"tokio", "1.50.0", true},
		{"serde", "1.0.219", true},
		{"addr2line", "0.24.2", true},
		{"nonexistent", "", false},
	}

	for _, tc := range tests {
		got, ok := resolved[tc.name]
		if ok != tc.present {
			t.Errorf("resolved[%q] present=%v, want %v", tc.name, ok, tc.present)
			continue
		}
		if tc.present && got != tc.want {
			t.Errorf("resolved[%q] = %q, want %q", tc.name, got, tc.want)
		}
	}
}

func TestParseCargoLock_Empty(t *testing.T) {
	t.Parallel()
	resolved := ParseCargoLock([]byte(""))
	if len(resolved) != 0 {
		t.Errorf("expected empty map, got %d entries", len(resolved))
	}
}

func TestParseCargoLock_NoTrailingNewline(t *testing.T) {
	t.Parallel()
	// Last block has no trailing blank line.
	input := `[[package]]
name = "hyper"
version = "1.5.0"
source = "registry+https://github.com/rust-lang/crates.io-index"`

	resolved := ParseCargoLock([]byte(input))
	if v, ok := resolved["hyper"]; !ok {
		t.Error("hyper not found in resolved map")
	} else if v != "1.5.0" {
		t.Errorf("hyper version = %q, want %q", v, "1.5.0")
	}
}

